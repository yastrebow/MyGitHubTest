public class MyCalculator {

    public static int add(int x, int y) {
        return x+y;
    }

    public static int sub(int x, int y) {
        return x-y;
    }

    public static int multi(int x, int y) {
        return x*y;
    }

    public static float div(float x, float y) {
        return x/y;
    }
}
